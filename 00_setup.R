    paste0('SEED          <- ', SEED),
    paste0('MAX_BILLS     <- ', MAX_BILLS),
    paste0('RAW_DIR       <- "', RAW_DIR, '"'),
    paste0('ACCENT_COLOR  <- "', ACCENT_COLOR, '"'),
    paste0('GRAY_DARK     <- "', GRAY_DARK, '"'),
    paste0('GRAY_MID      <- "', GRAY_MID, '"'),
    paste0('GRAY_LIGHT    <- "', GRAY_LIGHT, '"'),
    'TRIFECTA_COLORS <- c(R = "#B0281A", D = "#2C4770", Divided = "#6B6B6B")',
    "",
    "# Shared ggplot2 theme — source this file, then theme_set(bills_theme)",
    "library(tidyverse)",
    "bills_theme <- theme_minimal(base_size = 13) +",
    "  theme(",
    "    plot.background  = element_rect(fill = 'white', color = NA),",
    "    panel.background = element_rect(fill = 'white', color = NA),",
    "    panel.grid.major.y = element_line(color = '#E8E8E8', linewidth = 0.4),",
    "    panel.grid.major.x = element_blank(),",
    "    panel.grid.minor   = element_blank(),",
    "    axis.text  = element_text(color = '#555555', size = 10),",
    "    axis.title = element_text(color = '#1A1A1A', size = 11, face = 'bold'),",
    "    axis.ticks = element_blank(),",
    "    axis.line  = element_blank(),",
    "    plot.title    = element_text(color = '#1A1A1A', size = 14, face = 'bold',",
    "                                 margin = margin(b = 6)),",
    "    plot.subtitle = element_text(color = '#555555', size = 11,",
    "                                 margin = margin(b = 12)),",
    "    plot.caption  = element_text(color = '#555555', size = 8,",
    "                                 hjust = 0, margin = margin(t = 8)),",
    "    legend.position    = 'bottom',",
    "    legend.title       = element_text(color = '#1A1A1A', size = 10, face = 'bold'),",
    "    legend.text        = element_text(color = '#555555', size = 9),",
    "    legend.key.size    = unit(0.5, 'lines'),",
    "    legend.background  = element_rect(fill = 'white', color = NA),",
    "    strip.text       = element_text(color = '#1A1A1A', size = 10, face = 'bold'),",
    "    strip.background = element_rect(fill = '#E8E8E8', color = NA),",
    "    plot.margin = margin(12, 16, 12, 12)",
    "  )",
    "theme_set(bills_theme)"
  ),
  con = "global_constants.R"
)

cat("\nglobal_constants.R written.\n")

# ── Checkpoint ───────────────────────────────────────────────────────────────
# CHECKPOINT: Verify expected outputs before moving on
cat("\n── Setup complete ──────────────────────────────────────────\n")
cat("PDFs ready:", length(pdf_files), "\n")
cat("Folders:   ", paste(dirs, collapse = ", "), "\n")
cat("Constants: global_constants.R\n")
cat("Proceed to 01_metadata.R — but CONFIRM LEGISCAN_KEY first.\n")

message("00_setup.R complete.")

